package com.xyt.ygcf.model;


public class SearchResultInfo{
	private String logo;//图片
	private String id;//id
	private String shopName;//名称
	private String code;//编号
	private String grade;//等级
	private String domain;//商家图片域名
	private String productDomain;//产品图片域名
	private String longitude;//经度
	private String latitude;//纬度
	private String address;//地址
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getShopName() {
		return shopName;
	}
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	public String getProductDomain() {
		return productDomain;
	}
	public void setProductDomain(String productDomain) {
		this.productDomain = productDomain;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
}
